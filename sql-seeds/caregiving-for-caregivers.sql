<h2>What Is Caregiver Burnout?</h2>
<p>Caregiver burnout is a state of physical, emotional, and mental exhaustion caused by the prolonged stress of caregiving. Unlike ordinary tiredness, burnout does not go away after a good night's sleep. It can build gradually over months or years, often going unnoticed because caregivers tend to put their own needs last.</p>
<p>The American Psychological Association notes that family caregivers are significantly more likely to experience depression, anxiety, and chronic illness than non-caregivers. In Hawaii, where ohana (family) values often mean adult children take on caregiver roles quietly and without asking for help, burnout is especially common — and especially underreported.</p>

